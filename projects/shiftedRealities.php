            <div class="project-about__content">
              <div class="content project-about__text">
                <p>In Michael Baumgarten's imagery, objects have escaped the repetition of everyday life, to play in their own theatre; staged in castles, on country estates and in pleasure gardens sometimes even in simpler dwellings a field or at sea; immersing themselves in enchanted worlds.</p>
                <p>For many years, the pictorial illusionary spaces presented in this Shifted Realities publication have been created in the photographer's work, published in magazines or exhibited. Their elaborate stagings transpose a bathtub into the middle of a castle’s drawing room, plant landscaped gardens with bobbin lace, or enliven elegant interiors with worlds of oversized pop-up books like those we always loved in our childhood days. Elsewhere, books dance acrobatically, growing into a reading tree, bending as moving columns or propping up tables and beds to dynamically transform into inspiring libraries. <span class="dots">...</span></p>
              </div>
              <div class="content project-about__text-moore">
                <p class="is-hide">In a Lisbon pleasure garden, the famous blue and white azuleijos with their narratives and ornaments not only cover walls but shape a variety of things; furniture, musical instruments and even the sail of a stone boat in the fountain of an inner courtyard.</p>
                <p class="is-hide">These picture tiles become ceramic wallpaper; wallpaper is a continuous theme in the pictorial fantasy spaces of Michael Baumgarten's photographs.</p>
                <p class="is-hide">In other pictures as well, covering the simplicity of the familiar walls with a fantasy  life of its own. As camouflage, they envelop - or perhaps even deceive us about what is backdrop or canvas. This  is exactly the case in a castle filled with draped Trompe L'Oeil  furniture as if their real structures have seeped through the dust sheet protection. The motifs depicted on them play with our imagination and curiosity, leaving us uncertain  to whether what is visible is actually under the cover.</p>
                <p class="is-hide">Magic Plane is an early staging of Michael Baumgarten which depicts a yellow propellor plane that is full of intrinsic self assertion . It seems to be resisting the possible fate of ending up as a discarded object of dreams in a technology or aviation museum. </p>
                <p class="is-hide">The photograph gives the impression that it has originated from an early age where the dream to fly first originated. </p>
                <p class="is-hide">The plane  draped in a red velvet blanket with yellow tassels reminiscent of an opera diva with her stole.  She has rolled onto a field perhaps wanting to take to the skies once more, singing her aria with the sounds of the propeller, looking at the world with different eyes. </p>
                <p class="is-hide">An intention that also appears in the later photographs by Michael Baumgarten presented here, realized with poetic and pictorial inventiveness.</p>
                <p class="is-hide">Barbara Hofmann-Johnson is an art historian and curator of contemporary art and photography living in Germany. </p>
                <p class="is-hide">She is director at the Museum für Photographie Braunschweig, Lower Saxony.</p>
                <p class="is-hide">Barbara Hofmann-Johnson ist Kunsthistorikerin und Kuratorin für zeitgenössische Kunst und Fotografie und lebt in Deutschland. </p>
                <p class="is-hide">Sie ist Direktorin am Museum für Photographie Braunschweig, Niedersachsen.</p>

